
<h1> Dirac Spinor </h1>
<p>In quantum field theory, the Dirac spinor is the spinor that describes all known fundamental particles that are fermions, with the possible exception of neutrinos. It appears in the plane-wave solution to the Dirac equation, and is a certain combination of two Weyl spinors, specifically, a bispinor that transforms "spinorially" under the action of the Lorentz group.

    Dirac spinors are important and interesting in numerous ways. Foremost, they are important as they do describe all of the known fundamental particle fermions in nature; this includes the electron and the quarks. Algebraically they behave, in a certain sense, as the "square root" of a vector. This is not readily apparent from direct examination, but it has slowly become clear over the last 60 years that spinorial representations are fundamental to geometry. For example, effectively all Riemannian manifolds can have spinors and spin connections built upon them, via the Clifford algebra.[1] The Dirac spinor is specific to that of Minkowski spacetime and Lorentz transformations; the general case is quite similar.</p>
<p class="right">This article is devoted to the Dirac spinor in the Dirac representation. This corresponds to a specific representation of the gamma matrices, and is best suited for demonstrating the positive and negative energy solutions of the Dirac equation. There are other representations, most notably the chiral representation, which is better suited for demonstrating the chiral symmetry of the solutions to the Dirac equation. The chiral spinors may be written as linear combinations of the Dirac spinors presented below; thus, nothing is lost or gained, other than a change in perspective with regards to the discrete symmetries of the solutions.</p>
